def get_model(is_vip: bool) -> str:
    return VIP_MODEL if is_vip else FREE_MODEL


async def ask_claude(system: str, user_message: str, is_vip: bool = False) -> str:
    try:
        message = client.messages.create(
            model=get_model(is_vip),
            max_tokens=1500,
            system=system,
            messages=[{"role": "user", "content": user_message}],
        )
        return message.content[0].text
    except anthropic.RateLimitError:
        return "⚠️ Превышен лимит запросов к AI. Попробуй через минуту."
    except Exception as e:
        return f"⚠️ Ошибка AI: {str(e)[:100]}. Попробуй ещё раз."


async def ask_claude_with_image(
    system: str,
    user_message: str,
    image_data: bytes,
    media_type: str = "image/jpeg",
    is_vip: bool = False,
) -> str:
    try:
        image_b64 = base64.standard_b64encode(image_data).decode("utf-8")
        message = client.messages.create(
            model=get_model(is_vip),
            max_tokens=1500,
            system=system,
            messages=[{
                "role": "user",
                "content": [
                    {
                        "type": "image",
                        "source": {
                            "type": "base64",
                            "media_type": media_type,
                            "data": image_b64,
                        },
                    },
                    {"type": "text", "text": user_message},
                ],
            }],
        )
        return message.content[0].text
    except anthropic.RateLimitError:
        return "⚠️ Превышен лимит запросов к AI. Попробуй через минуту."
    except Exception as e:
        return f"⚠️ Ошибка AI: {str(e)[:100]}. Попробуй ещё раз."
