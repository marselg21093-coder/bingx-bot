def send(text: str) -> None:
    url = f"https://api.telegram.org/bot{BOT_TOKEN}/sendMessage"
    resp = requests.post(url, json={
        "chat_id": CHANNEL_ID,
        "text": text,
        "parse_mode": "HTML",
        "disable_web_page_preview": True,
    }, timeout=10)
    result = resp.json()
    if result.get("ok"):
        print("✅ Пост отправлен")
    else:
        print(f"❌ Ошибка: {result.get('description')}")
        sys.exit(1)


