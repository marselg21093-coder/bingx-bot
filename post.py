def get_prices() -> str:
    try:
        r = requests.get(
            "https://api.coingecko.com/api/v3/simple/price",
            params={"ids": "bitcoin,ethereum", "vs_currencies": "usd",
                    "include_24hr_change": "true"},
            timeout=10,
        )
        d = r.json()
        btc     = d["bitcoin"]["usd"]
        btc_ch  = d["bitcoin"]["usd_24h_change"]
        eth     = d["ethereum"]["usd"]
        eth_ch  = d["ethereum"]["usd_24h_change"]
        btc_arr = "📈" if btc_ch >= 0 else "📉"
        eth_arr = "📈" if eth_ch >= 0 else "📉"
        return (
            f"\n\n📊 <b>Курс прямо сейчас:</b>\n"
            f"🟠 BTC: <b>${btc:,.0f}</b> {btc_arr} {btc_ch:+.1f}%\n"
            f"🔵 ETH: <b>${eth:,.0f}</b> {eth_arr} {eth_ch:+.1f}%\n\n"
            f"🔗 Торгуй на BingX 👉 {REF_LINK}"
        )
    except Exception:
        return f"\n\n🔗 Торгуй на BingX 👉 {REF_LINK}"


# ─── ОТПРАВКА ─────────────────────────────────────────────────────────────────

def send_photo(text: str) -> None:
    # Картинка выбирается по дню года — каждый день другая
    day = datetime.date.today().timetuple().tm_yday
    image_url = IMAGES[day % len(IMAGES)]

    if len(text) > 1020:
        text = text[:1017] + "..."

    r = requests.post(
        f"https://api.telegram.org/bot{BOT_TOKEN}/sendPhoto",
        json={"chat_id": CHANNEL_ID, "photo": image_url,
              "caption": text, "parse_mode": "HTML"},
        timeout=15,
    )
    result = r.json()
    if result.get("ok"):
        print("✅ Пост отправлен")
    else:
        print(f"⚠️ Фото не загрузилось, отправляю текстом: {result.get('description')}")
        r2 = requests.post(
            f"https://api.telegram.org/bot{BOT_TOKEN}/sendMessage",
            json={"chat_id": CHANNEL_ID, "text": text,
                  "parse_mode": "HTML", "disable_web_page_preview": True},
            timeout=10,
        )
        if not r2.json().get("ok"):
            print(f"❌ Ошибка: {r2.json().get('description')}")
            sys.exit(1)


