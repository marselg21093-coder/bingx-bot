def get_prices() -> str:
    try:
        resp = requests.get(
            "https://api.coingecko.com/api/v3/simple/price",
            params={"ids": "bitcoin,ethereum", "vs_currencies": "usd"},
            timeout=10,
        )
        data = resp.json()
        btc = data["bitcoin"]["usd"]
        eth = data["ethereum"]["usd"]
        return (
            f"\n\n📊 <b>Курс прямо сейчас:</b>\n"
            f"🟠 BTC: <b>${btc:,.0f}</b>\n"
            f"🔵 ETH: <b>${eth:,.0f}</b>"
        )
    except Exception as e:
        print(f"Не удалось получить курс: {e}")
        return ""


def send_photo(caption: str) -> None:
    image_url = random.choice(IMAGES)
    url = f"https://api.telegram.org/bot{BOT_TOKEN}/sendPhoto"
    resp = requests.post(
        url,
        json={
            "chat_id": CHANNEL_ID,
            "photo": image_url,
            "caption": caption,
            "parse_mode": "HTML",
        },
        timeout=15,
    )
    result = resp.json()
    if result.get("ok"):
        print("✅ Пост с картинкой отправлен")
    else:
        print(f"⚠️ Картинка не загрузилась, отправляю текстом: {result.get('description')}")
        send_text(caption)


def send_text(caption: str) -> None:
    url = f"https://api.telegram.org/bot{BOT_TOKEN}/sendMessage"
    resp = requests.post(
        url,
        json={
            "chat_id": CHANNEL_ID,
            "text": caption,
            "parse_mode": "HTML",
            "disable_web_page_preview": True,
        },
        timeout=10,
    )
    result = resp.json()
    if result.get("ok"):
        print("✅ Текстовый пост отправлен")
    else:
        print(f"❌ Ошибка: {result.get('description')}")
        sys.exit(1)


