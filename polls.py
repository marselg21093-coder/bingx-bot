# ─── ОТПРАВКА ─────────────────────────────────────────────────────────────────

def send_poll(poll: dict) -> None:
    url = f"https://api.telegram.org/bot{BOT_TOKEN}/sendPoll"
    resp = requests.post(
        url,
        json={
            "chat_id": CHANNEL_ID,
            "question": poll["question"],
            "options": poll["options"],
            "is_anonymous": True,
            "allows_multiple_answers": False,
        },
        timeout=10,
    )
    result = resp.json()
    if result.get("ok"):
        print(f"✅ Опрос отправлен: {poll['question'][:50]}...")
    else:
        print(f"❌ Ошибка: {result.get('description')}")
        sys.exit(1)


